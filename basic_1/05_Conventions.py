"""Python Conventions"""

# 1. Variable Naming Conventions
# Use Lowercase (minuscula) letters and undercores to separate words in variable names. Avoid using special characters and spaces.

my_variable = 10
my_string = "Hello, World!"

# 2. Function Naming Conventions
# Use Lowercase (minuscula) letters and undercores to separate words in function names. Avoid using special characters and spaces.

def my_function():
    pass

# 3. Class Naming Conventions
# Use PascalCase (ComienzaConMayuscula) to name classes. Avoid using special characters and spaces.

class MyClass:
    def __init__(self):
        self.my_attribute = 10

# 4. Constant Naming Conventions
# Use Uppercase (MAYUSCULA) letters and undercores to separate words in constant names. Avoid using special characters and spaces.

PI = 3.14159
SPEED_OF_LIGHT = 299792458


